import ReactDOM from "react-dom";
import useFetch from "./apiCallsHook";
const CommentsData = () => {
  const [data] = useFetch("https://jsonplaceholder.typicode.com/todos");

  return (
    <>
      {data &&
        data.map((item) => {
          return <p key={item.id}>{item.title}</p>;
        })}
    </>
  );
};

export default CommentsData;